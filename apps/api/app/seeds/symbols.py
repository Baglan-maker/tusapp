def seed() -> int:
    """Upsert all symbols by slug. Returns the number of rows processed."""
    with SessionLocal() as session:
        for item in SYMBOLS:
            stmt = insert(Symbol).values(
                slug=item["slug"],
                names=item["names"],
                lens_meanings=item["lens_meanings"],
            )
            stmt = stmt.on_conflict_do_update(
                index_elements=[Symbol.slug],
                set_={
                    "names": stmt.excluded.names,
                    "lens_meanings": stmt.excluded.lens_meanings,
                },
            )
            session.execute(stmt)
        session.commit()
    return len(SYMBOLS)


if __name__ == "__main__":
    count = seed()
    print(f"Seeded {count} symbols.")
